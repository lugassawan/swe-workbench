class TestArgParsing:
    """Arg-parsing surface — run the real script directly, no stubs needed.

    The arg loop (script lines 7–26) exits before the python3.12 preflight
    (line 34), so these pass even in environments where Python 3.12 is absent.
    """

    def test_help_exits_zero_and_prints_usage(self):
        result = subprocess.run(
            ["bash", str(LOCK_SCRIPT), "--help"],
            capture_output=True,
            text=True,
            env=_CLEAN_ENV,
        )
        assert result.returncode == 0
        assert "Usage:" in result.stdout

    def test_short_help_flag_exits_zero(self):
        result = subprocess.run(
            ["bash", str(LOCK_SCRIPT), "-h"],
            capture_output=True,
            text=True,
            env=_CLEAN_ENV,
        )
        assert result.returncode == 0

    def test_unknown_arg_exits_one(self):
        result = subprocess.run(
            ["bash", str(LOCK_SCRIPT), "--bogus"],
            capture_output=True,
            text=True,
            env=_CLEAN_ENV,
        )
        assert result.returncode == 1
        assert "unknown argument" in result.stderr


class TestCheckDriftGate:
    """Behavioral tests for the --check lockfile-drift gate.

    Each test scaffolds a hermetic temp repo (no network, no Python 3.12).
    The stub python3.12 creates a fake venv so the script's compile() function
    runs fully, exercising the grep-strip + diff comparison at lines 77–82.
    """

    def _env(self, paths: dict, *, drift: bool = False) -> dict:
        return {
            **_CLEAN_ENV,
            "PATH": f"{paths['stub_dir']}:{_CLEAN_ENV['PATH']}",
            "STUB_DIR": str(paths["stub_dir"]),
            "DRIFT_TEST": "1" if drift else "0",
        }

    def test_check_exits_zero_when_in_sync(self, tmp_path):
        """Fresh compile output matches committed lockfiles → exit 0."""
        paths = _make_repo(tmp_path)
        result = subprocess.run(
            ["bash", str(paths["script"]), "--check"],
            capture_output=True,
            text=True,
            env=self._env(paths),
        )
        assert result.returncode == 0, result.stderr
        assert "up to date" in result.stdout
        assert result.stdout.count("up to date") == 3

    def test_check_exits_nonzero_on_drift(self, tmp_path):
        """Fresh compile has an extra dependency line → exit non-zero."""
        paths = _make_repo(tmp_path)
        result = subprocess.run(
            ["bash", str(paths["script"]), "--check"],
            capture_output=True,
            text=True,
            env=self._env(paths, drift=True),
        )
        assert result.returncode == 1
        assert "out of date" in result.stderr

    def test_check_ignores_header_and_blank_diffs(self, tmp_path):
        """Extra column-0 # lines and blank lines in committed locks are ignored.

        Verifies the core concern of issue #308: the grep -v '^#' stripping
        and --ignore-blank-lines flag prevent false positives when committed
        lockfiles have more header content than fresh pip-compile --no-header
        output.  The stub outputs headers-stripped content; the committed files
        retain the extra headers.  If the script's grep-strip were removed from
        the existing_stripped side, those headers would survive into the diff
        and the test would fail — so this assertion is non-vacuous.
        """
        paths = _make_repo(tmp_path)
        tests_dir = paths["repo"] / "tests"
        for name in ["requirements", "release-requirements", "build-requirements"]:
            lock_file = tests_dir / f"{name}.lock"
            lock_file.write_text(
                "# Extra header added by test\n"
                "# Another comment\n"
                "\n"
                + lock_file.read_text()
            )
        result = subprocess.run(
            ["bash", str(paths["script"]), "--check"],
            capture_output=True,
            text=True,
            env=self._env(paths),
        )
        assert result.returncode == 0, result.stderr


class TestGenerateRefreshesStalePins:
    """Behavioral test for generate-mode stale-pin refresh (issue #481).

    Generate mode compiles with --output-file pointed directly at the
    existing lockfile, so pip-compile treats still-valid pins as a
    resolution seed and keeps them — a lockfile that has drifted from
    upstream is re-affirmed instead of refreshed.  This seeds a lock with a
    stale-but-still-valid pin (fakepkg==1.0.0) and asserts that running the
    script without --check refreshes it to the latest resolution
    (fakepkg==1.1.0), proving the generate branch performs a fresh
    re-resolution rather than reusing the seed.
    """

    def test_generate_refreshes_stale_pin(self, tmp_path):
        paths = _make_repo(tmp_path, upgrade_aware=True)
        lock_file = paths["repo"] / "tests" / "requirements.lock"
        assert "fakepkg==1.0.0" in lock_file.read_text()

        env = {
            **_CLEAN_ENV,
            "PATH": f"{paths['stub_dir']}:{_CLEAN_ENV['PATH']}",
            "STUB_DIR": str(paths["stub_dir"]),
            "LATEST_FIXTURE": str(paths["latest_fixture"]),
        }
        result = subprocess.run(
            ["bash", str(paths["script"])],
            capture_output=True,
            text=True,
            env=env,
        )

        assert result.returncode == 0, result.stderr
        refreshed = lock_file.read_text()
        assert "fakepkg==1.1.0" in refreshed
        assert "fakepkg==1.0.0" not in refreshed
